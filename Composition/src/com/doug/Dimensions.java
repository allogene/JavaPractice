public class Dimensions {
    private int wideth, height, depth;

    public Dimensions(int wideth, int height, int depth) {
        this.wideth = wideth;
        this.height = height;
        this.depth = depth;
    }

    public int getWideth() {
        return wideth;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }
}
